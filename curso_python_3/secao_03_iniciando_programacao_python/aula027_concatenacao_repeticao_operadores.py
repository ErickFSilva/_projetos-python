# Concatenação
concatenacao = 'A' + 'B' + 'C'
print(concatenacao)

# Concatenação com coerção
concatenacao_coercao = 'Luiz' + str(1) + ' ' + 'Otávio'
print(concatenacao_coercao)

# Repetição, utilizando o sinal de multiplicação (*)
a_dez_vezes = 'A' * 10
print(a_dez_vezes)

tres_vezes_luiz = 3 * 'Luiz'
print(tres_vezes_luiz)